package com.SearchEngine;

public class HistoryResult {
    String keyword;
    String link;

//    Getter method for retrieving the keyword.
    public String getKeyword() {
        return keyword;
    }
//    Setter method for setting the keyword.
    public void setKeyword(String keyword) {
        this.keyword = keyword;
    }
//    Getter method for retrieving the link.
    public String getLink() {
        return link;
    }
//    Setter method for setting the link.
    public void setLink(String link) {
        this.link = link;
    }
}
